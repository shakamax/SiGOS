public class ListaEquipamento {
    private String Defeito;
    private String Equipamento;
    private String Acessório;
    private String Observacao;

    /**
     * @return the Defeito
     */
    public String getDefeito() {
        return Defeito;
    }

    /**
     * @param Defeito the Defeito to set
     */
    public void setDefeito(String Defeito) {
        this.Defeito = Defeito;
    }

    /**
     * @return the Equipamento
     */
    public String getEquipamento() {
        return Equipamento;
    }

    /**
     * @param Equipamento the Equipamento to set
     */
    public void setEquipamento(String Equipamento) {
        this.Equipamento = Equipamento;
    }

    /**
     * @return the Acessório
     */
    public String getAcessório() {
        return Acessório;
    }

    /**
     * @param Acessório the Acessório to set
     */
    public void setAcessório(String Acessório) {
        this.Acessório = Acessório;
    }

    /**
     * @return the Observacao
     */
    public String getObservacao() {
        return Observacao;
    }

    /**
     * @param Observacao the Observacao to set
     */
    public void setObservacao(String Observacao) {
        this.Observacao = Observacao;
    }
    
}
